    private void btn_crearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_crearActionPerformed

        if(!txtF_nombre.getText().equals("") && !txtF_genero.getText().equals("") && !txtF_edad.getText().equals("") && !txtF_ipv4.getText().equals("")){
            String nombre = txtF_nombre.getText();
            String genero = txtF_genero.getText();
            int edad = Integer.parseInt(txtF_edad.getText());
            String ipv4 = txtF_ipv4.getText();
            Persona persona = new Persona(nombre, genero, edad, ipv4);

            hashtable.put(persona.getNombre()+"-"+persona.getEdad(),     persona);

            model.addRow(new Object[]{
                    persona.getNombre(),
                    persona.getGenero(),
                    persona.getEdad(),
                    persona.getIpv4()
            });
            tbl_listadohash.setModel(model);


            txtF_nombre.setText("");
            txtF_genero.setText("");
            txtF_edad.setText("");
            txtF_ipv4.setText("");
        }

    }//GEN-LAST:event_btn_crearActionPerformed

    private void txtF_buscarKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtF_buscarKeyPressed
        if(evt.getKeyCode() == 10){
            if(!txtF_buscar.getText().equals("")){
                String buscar = txtF_buscar.getText();
                Persona personaRecuperada = hashtable.get(buscar);
                if (personaRecuperada != null) {
                    txtA_encontrado.setText(""+personaRecuperada);
                } else {
                    txtA_encontrado.setText("No se encontró ninguna \npersona con el codigo: \n" + buscar);
                }
            }
        }
    }//GEN-LAST:event_txtF_buscarKeyPressed

