    public void obteniendoJSON(){
        // de StackOverflow -> para la lectura del archivo JSON:
        List<Persona> personas = null;
        String filePath = "src/main/java/APF3/P3/DATA.json";
        try (Reader reader = new FileReader(filePath)) {
            java.lang.reflect.Type listType = new TypeToken<List<Persona>>() {}.getType();
            personas = new Gson().fromJson(reader, listType);
        } catch (Exception e) { e.printStackTrace(); }

        /*
         *  A partir de Java 8, se recomienda usar HashMap en lugar de Hashtable debido a que HashMap
         *  no es sincronizado y,por lo tanto, suele ser más eficiente en entornos no concurrentes.
         * */

        // Volcando los objetos Persona en la Hashtable
        for (Persona persona : personas){
            hashtable.put(persona.getNombre()+"-"+persona.getEdad(), persona);
        }
    }
    public void cargarTabla(){
        model.setRowCount(0);
        model.setColumnCount(0);
        
        model.addColumn("Nombre");
        model.addColumn("Genero");
        model.addColumn("Edad");
        model.addColumn("IPv4");
        for (Persona persona : hashtable.values()) {
            model.addRow(new Object[]{
                    persona.getNombre(),
                    persona.getGenero(),
                    persona.getEdad(),
                    persona.getIpv4()
            });
        }
        tbl_listadohash.setModel(model);
    }

